"""Renders a run's output.json (+ bugs.json, if present) as a single self-contained
HTML file - everything the JSON contains, laid out for a human to actually read
phase by phase, instead of scrolling raw JSON. Auto-generated by run_live.py at the
end of every run; also runnable standalone against an existing results/ directory.

Structurally the same design as live-sut-poc/report.py (same palette/type system,
same checkpoint/round grouping, same markdown-lite rendering, same fixes for the
two logic bugs found in that report's review), adapted for this SUT's different
data shape: schema doc + one happy-day example instead of 8 baseline calls, and
request_count/concurrent/accepted_count/limit instead of text/latency.
"""

import html
import json
import re
from pathlib import Path


def _esc(value) -> str:
    if value is None:
        return ""
    return html.escape(str(value))


_BOLD_RE = re.compile(r"\*\*(.+?)\*\*")
_CODE_RE = re.compile(r"`(.+?)`")
_HEADER_RE = re.compile(r"^(#{1,6})\s+(.*)")
_BULLET_RE = re.compile(r"^[-*]\s+(.*)")


def _inline_markdown(text: str) -> str:
    escaped = _esc(text)
    escaped = _BOLD_RE.sub(r"<strong>\1</strong>", escaped)
    escaped = _CODE_RE.sub(r"<code>\1</code>", escaped)
    return escaped


def _render_prose(text) -> str:
    """The Driver and Skeptic write markdown-flavored prose (headers, **bold**,
    `code`, bullet lists) in every free-text field - this is a small, deliberately
    narrow markdown->HTML pass covering just what these fields actually contain,
    not a general markdown parser."""
    if not text:
        return ""
    html_parts = []
    list_buffer = []
    paragraph_buffer = []

    def flush_list():
        if list_buffer:
            html_parts.append("<ul>" + "".join(f"<li>{item}</li>" for item in list_buffer) + "</ul>")
            list_buffer.clear()

    def flush_paragraph():
        if paragraph_buffer:
            html_parts.append(f"<p>{' '.join(paragraph_buffer)}</p>")
            paragraph_buffer.clear()

    for raw_line in str(text).strip().split("\n"):
        line = raw_line.strip()
        if not line:
            flush_paragraph()
            flush_list()
            continue

        header_match = _HEADER_RE.match(line)
        if header_match:
            flush_paragraph()
            flush_list()
            level = min(len(header_match.group(1)) + 3, 6)
            html_parts.append(f"<h{level}>{_inline_markdown(header_match.group(2))}</h{level}>")
            continue

        bullet_match = _BULLET_RE.match(line)
        if bullet_match:
            flush_paragraph()
            list_buffer.append(_inline_markdown(bullet_match.group(1)))
            continue

        flush_list()
        paragraph_buffer.append(_inline_markdown(line))

    flush_paragraph()
    flush_list()
    return "".join(html_parts)


def _badge(text, kind) -> str:
    return f'<span class="badge badge-{kind}">{_esc(text)}</span>'


def _correctness_badge(correctness) -> str:
    if correctness == "correct":
        return _badge("correct", "good")
    if correctness == "overcounted":
        return _badge("overcounted", "bad")
    return _badge(correctness or "unknown", "warn")


def _bool_badge(value, true_label="yes", false_label="no") -> str:
    if value is True:
        return _badge(true_label, "good")
    if value is False:
        return _badge(false_label, "bad")
    return _badge("unknown", "warn")


def _verdict_badge(verdict) -> str:
    kind = {
        "corroborated": "good",
        "refuted": "bad",
        "inconclusive": "warn",
        "well_supported": "good",
        "premature": "warn",
        "holds_up": "good",
        "weak": "bad",
        "genuine": "good",
        "strawman": "bad",
    }.get(verdict, "warn")
    return _badge(verdict.replace("_", " ") if verdict else "unknown", kind)


def _render_json_block(data) -> str:
    return f'<pre class="payload">{_esc(json.dumps(data, ensure_ascii=False))}</pre>'


def _render_schema_section(api_schema, happy_day_example) -> str:
    happy_request = (happy_day_example or {}).get("request", {})
    happy_response = (happy_day_example or {}).get("response", {})
    return f"""
    <div class="exhibit">
      <h3>API schema</h3>
      <pre class="schema-doc">{_esc(api_schema)}</pre>
    </div>
    <div class="exhibit">
      <h3>Happy-day example</h3>
      <p class="eyebrow">Request</p>
      {_render_json_block(happy_request.get('body', {}))}
      <p class="eyebrow">Response</p>
      {_render_json_block(happy_response.get('body', {}))}
    </div>
    """


def _render_test_entry(entry, context_label=None) -> str:
    """context_label overrides the hypothesis line for tests that never carry a
    linked_hypothesis field at all - confirm/disconfirm/follow-up tests come
    straight from execute_test(), which has no such field (only casting_log
    entries get one, added by the caller in run_checkpoint_cycle)."""
    if not entry:
        return ""
    request = entry.get("request", {})
    body = request.get("body", {})
    request_count = request.get("request_count", 1)
    mode = "concurrent" if request.get("concurrent") else "sequential"
    mode_html = f'<span class="test-number">{_esc(request_count)} req &middot; {_esc(mode)}</span>'

    if context_label:
        linked_html = f"<strong>{_esc(context_label)}</strong>"
    else:
        linked = entry.get("linked_hypothesis")
        linked_html = f"<strong>Hypothesis:</strong> {_esc(linked)}" if linked else (
            '<span class="probe-label">Edge-case probe</span> (no linked hypothesis)'
        )
    test_number = entry.get("test_number")
    number_html = f'<span class="test-number">Test #{_esc(test_number)}</span>' if test_number is not None else ""

    if entry.get("skipped"):
        return f"""
        <article class="test test-skipped">
          <div class="test-hypothesis">{number_html}{mode_html}{linked_html}</div>
          {_render_json_block(body)}
          <div class="test-outcome">{_badge('skipped', 'warn')} {_inline_markdown(entry.get('skip_reason'))}</div>
        </article>
        """

    responses = entry.get("responses", [])
    used_values = [r["body"].get("used") for r in responses if isinstance(r.get("body"), dict) and "used" in r["body"]]
    matched = entry.get("prediction_matched")
    return f"""
    <article class="test">
      <div class="test-hypothesis">{number_html}{mode_html}{linked_html}</div>
      {_render_json_block(body)}
      <div class="test-predicted">Predicted: {_inline_markdown(entry.get('predicted_outcome'))}
        {_correctness_badge(entry.get('predicted_correctness'))}</div>
      <div class="test-outcome">
        Accepted <span class="num">{_esc(entry.get('accepted_count'))}/{_esc(request_count)}</span>
        (limit <span class="num">{_esc(entry.get('limit'))}</span>)
        {_correctness_badge(entry.get('actual_correctness'))}
        <span class="sep">&middot;</span> prediction {_bool_badge(matched, 'matched', 'missed')}
      </div>
      <div class="test-outcome prose-muted">used values across responses: <span class="num">{_esc(', '.join(str(u) for u in used_values))}</span></div>
    </article>
    """


def _render_casting_section(casting_log, behavior_checkpoints) -> str:
    by_checkpoint = {}
    for entry in casting_log:
        by_checkpoint.setdefault(entry["checkpoint"], {}).setdefault(entry["round"], []).append(entry)

    behavior_by_checkpoint = {bc["checkpoint"]: bc for bc in behavior_checkpoints}

    # A checkpoint that gave up on its very first round proposes zero tests, so it
    # never contributes anything to casting_log - but it still gets a behavior
    # hypothesis + Skeptic review. Iterating only over by_checkpoint's keys would
    # silently drop that checkpoint's conclusion entirely, even though it's real,
    # generated data sitting in behavior_checkpoints.
    all_checkpoint_nums = sorted(set(by_checkpoint) | set(behavior_by_checkpoint))

    parts = []
    for checkpoint_num in all_checkpoint_nums:
        rounds = by_checkpoint.get(checkpoint_num, {})
        round_html = []
        for round_num in sorted(rounds):
            entries = rounds[round_num]
            reasoning = entries[0].get("round_reasoning", "")
            tests_html = "".join(_render_test_entry(e) for e in entries)
            round_html.append(f"""
            <div class="round">
              <p class="eyebrow">Round {round_num}</p>
              <details class="reasoning" open>
                <summary>Reasoning</summary>
                <div class="prose">{_render_prose(reasoning)}</div>
              </details>
              <div class="test-grid">{tests_html}</div>
            </div>
            """)
        if not rounds:
            round_html.append('<p class="prose-muted">No rounds executed - the Driver gave up immediately at the start of this checkpoint.</p>')

        behavior_html = ""
        bc = behavior_by_checkpoint.get(checkpoint_num)
        if bc:
            bh = bc["behavior_hypothesis"]
            bsr = bc["behavior_skeptic_review"]
            untested = "".join(f"<li>{_inline_markdown(a)}</li>" for a in bh.get("untested_areas", []))
            gaps = "".join(f"<li>{_inline_markdown(g)}</li>" for g in bsr.get("gaps", []))
            behavior_html = f"""
            <div class="exhibit">
              <p class="eyebrow">Checkpoint {checkpoint_num} conclusion &middot; nothing anomalous found in its rounds</p>
              <h4>Behavior hypothesis</h4>
              <div class="prose">{_render_prose(bh.get('observed_behavior'))}</div>
              <p><strong>Untested areas named by the Driver</strong></p>
              <ul>{untested}</ul>
              <h4>Behavior-Skeptic review {_verdict_badge(bsr.get('assessment'))}</h4>
              <p><strong>Gaps identified</strong></p>
              <ul>{gaps}</ul>
              <div class="prose prose-muted">{_render_prose(bsr.get('reasoning'))}</div>
            </div>
            """

        parts.append(f"""
        <div class="checkpoint">
          <h3>Checkpoint {checkpoint_num}</h3>
          {''.join(round_html)}
          {behavior_html}
        </div>
        """)
    return "".join(parts)


def _render_followup_rounds(followup_rounds) -> str:
    parts = []
    for i, round_entry in enumerate(followup_rounds, start=1):
        test_html = ""
        if "test_result" in round_entry:
            test_html = _render_test_entry(round_entry["test_result"], context_label=f"Follow-up test (round {i})")
        parts.append(f"""
        <div class="followup-round">
          <p class="eyebrow">Follow-up round {i}
            {_verdict_badge(round_entry.get('verdict'))}
            <span class="sep">&middot;</span> continuing: {_bool_badge(round_entry.get('continue_investigation'))}</p>
          <div class="prose">{_render_prose(round_entry.get('reasoning'))}</div>
          {test_html}
        </div>
        """)
    return "".join(parts)


def _render_hypothesis_attempts(attempts) -> str:
    """Only the attempts Skeptic rejected before the one actually used - the final
    attempt is already shown in the main Claim/Skeptic exhibits below, so
    repeating it here would be redundant."""
    rejected = attempts[:-1] if len(attempts) > 1 else []
    if not rejected:
        return ""
    parts = []
    for i, attempt in enumerate(rejected, start=1):
        h = attempt["hypothesis"]
        sr = attempt["skeptic_review"]
        parts.append(f"""
        <div class="exhibit">
          <p class="eyebrow">Hypothesis attempt {i} {_verdict_badge(sr.get('skeptic_verdict'))} &middot; sent back for revision</p>
          <p><strong>Claim</strong></p>
          <div class="prose">{_render_prose(h.get('claim'))}</div>
          <p><strong>Why Skeptic rejected it</strong></p>
          <div class="prose prose-muted">{_render_prose(sr.get('reasoning'))}</div>
        </div>
        """)
    return f"""
    <div class="checkpoint">
      <h3>Hypothesis revisions</h3>
      <p class="prose-muted">Skeptic found the initial claim(s) too weak to spend test budget on - the Driver had to revise before confirm/disconfirm ran.</p>
      {''.join(parts)}
    </div>
    """


def _render_investigation_section(output) -> str:
    hypothesis = output.get("hypothesis")
    if not hypothesis:
        return ""

    skeptic = output.get("skeptic_review", {})
    strategies = "".join(f"<li>{_inline_markdown(s)}</li>" for s in skeptic.get("disproof_strategies", []))
    attempts = output.get("hypothesis_attempts", [])
    revisions_html = _render_hypothesis_attempts(attempts)
    claim_heading = "Claim (final, after revision)" if len(attempts) > 1 else "Claim"

    return f"""
    <section id="investigation">
      <p class="eyebrow">Phase 3</p>
      <h2>Investigation</h2>

      {revisions_html}

      <div class="exhibit">
        <h3>{claim_heading}</h3>
        <p><strong>Observed pattern</strong></p>
        <div class="prose">{_render_prose(hypothesis.get('observed_pattern'))}</div>
        <p><strong>Anomalous test number</strong><br><span class="num">{_esc(hypothesis.get('anomalous_test_number'))}</span></p>
        <p><strong>Claim</strong></p>
        <div class="prose">{_render_prose(hypothesis.get('claim'))}</div>
        <p><strong>Competing explanation</strong></p>
        <div class="prose">{_render_prose(hypothesis.get('competing_explanation'))}</div>
        <p><strong>Severity if true</strong> {_badge(hypothesis.get('severity_if_true'), 'bad' if hypothesis.get('severity_if_true') == 'high' else 'warn')}</p>
      </div>

      <div class="exhibit">
        <h3>Skeptic review of the claim
          {_verdict_badge(skeptic.get('skeptic_verdict'))}
        </h3>
        <p><strong>Competing explanation assessed as</strong> {_verdict_badge(skeptic.get('competing_explanation_assessment'))}</p>
        <p><strong>Skeptic's own alternative</strong></p>
        <div class="prose">{_render_prose(skeptic.get('skeptic_alternative'))}</div>
        <p><strong>Disproof strategies proposed</strong></p>
        <ul>{strategies}</ul>
        <div class="prose prose-muted">{_render_prose(skeptic.get('reasoning'))}</div>
      </div>

      <div class="checkpoint">
        <h3>Confirm / disconfirm</h3>
        <p class="eyebrow">Confirm test</p>
        {_render_test_entry(output.get('confirm_result', {}), context_label="Testing the claim above")}
        <p class="eyebrow">Disconfirm test</p>
        {_render_test_entry(output.get('disconfirm_result', {}), context_label="Testing the claim above")}
      </div>

      <div class="checkpoint">
        <h3>Follow-up rounds</h3>
        <p class="prose-muted">Stopped: {_esc(output.get('followup_stopped_reason'))}</p>
        {_render_followup_rounds(output.get('followup_rounds', []))}
      </div>
    </section>
    """


def _render_bug_report_section(bug_report) -> str:
    if not bug_report:
        return ""
    steps = "".join(f"<li>{_inline_markdown(s)}</li>" for s in bug_report.get("steps_to_reproduce", []))
    severity = bug_report.get("severity")
    status = bug_report.get("status")
    return f"""
    <section id="bug-report">
      <p class="eyebrow">Phase 4</p>
      <h2>Bug report</h2>
      <div class="exhibit exhibit-final">
        <h3>{_esc(bug_report.get('title'))}</h3>
        <p>{_badge(severity, 'bad' if severity == 'high' else 'warn')}
           {_badge(status, 'good' if status == 'corroborated' else 'warn')}</p>
        <div class="prose">{_render_prose(bug_report.get('description'))}</div>
        <p><strong>Steps to reproduce</strong></p>
        <ol>{steps}</ol>
        <p><strong>Expected behavior</strong></p>
        <div class="prose">{_render_prose(bug_report.get('expected_behavior'))}</div>
        <p><strong>Actual behavior</strong></p>
        <div class="prose">{_render_prose(bug_report.get('actual_behavior'))}</div>
        <div class="caveats">
          <p><strong>Caveats</strong></p>
          <div class="prose">{_render_prose(bug_report.get('caveats'))}</div>
        </div>
      </div>
    </section>
    """


