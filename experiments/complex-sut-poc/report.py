def render_report_from_dir(results_dir: Path) -> str:
    output = json.loads((results_dir / "output.json").read_text(encoding="utf-8"))
    bugs_path = results_dir / "bugs.json"
    bug_report = json.loads(bugs_path.read_text(encoding="utf-8")) if bugs_path.exists() else None
    return render_report(output, bug_report)


